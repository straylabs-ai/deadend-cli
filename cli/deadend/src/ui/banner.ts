export function createBanner(renderer: CliRenderer): BoxRenderable {
  const banner = new BoxRenderable(renderer, {
    width: "100%",
    flexDirection: "column",
    alignItems: "flex-start",
    marginBottom: 1,
  });

  banner.add(new TextRenderable(renderer, { content: "" }));

  for (const line of BANNER_LINES) {
    banner.add(
      new TextRenderable(renderer, {
        content: line,
        fg: theme.accent,
      }),
    );
  }

  return banner;
}
